export const REGEX = {
  decimalZeroTrim: /\.0+$|(\.[0-9]*[1-9])0+$/,
  email: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
  password:
    /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&^#(){}[\]:;<>,.?/~_+\-=|\\])[A-Za-z\d@$!%*?&^#(){}[\]:;<>,.?/~_+\-=|\\]{8,}$/,
  phone: /^(?:\+?(\d{1,3}))?[-.\s]?(\d{2,4})[-.\s]?(\d{3,4})[-.\s]?(\d{3,4})$/,
};
